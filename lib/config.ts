export function getWhatsAppLink(customMessage?: string): string {
  if (CLINIC_INFO.whatsappLink) {
    return CLINIC_INFO.whatsappLink;
  }

  const message = customMessage || CLINIC_INFO.whatsappMessage;
  const encodedMessage = encodeURIComponent(message);
  return `https://wa.me/${CLINIC_INFO.whatsappNumber}?text=${encodedMessage}`;
}
